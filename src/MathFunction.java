public class MathFunction {

    public static void main(String[] args) {
        System.out.println(Math.pow(2, 3)); // 2 в шестнадцатой степени
        System.out.println(Math.abs(10 -30)); // модуль числа (функция дающая только положительное значение)
        System.out.println(Math.floor(3.10)); // Функция отбрасывающая дробную часть
        System.out.println(Math.random()); // Функция выводящая рандомное значения от 0 до 1

    }
}
